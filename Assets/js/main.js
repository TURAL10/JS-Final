
function toggleDropdown() {
    if (document.getElementById("dropdownList").style.display === "block") {
        document.getElementById("dropdownList").style.display = "none";
        
    } else {
        document.getElementById("dropdownList").style.display = "block";
    }
}
function toggleDrop() {
    if (document.getElementById("dropdownList").style.display === "block") {
        document.getElementById("dropdownList").style.display = "none";
        
    } else {
        document.getElementById("dropdownList").style.display = "none";
    }
}
